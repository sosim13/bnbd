-- 테이블
CREATE TABLE SCOTT.BOARD
(
    BOARDNO             NUMBER NOT NULL,
    TITLE               VARCHAR2(100),
    CONTENTS            VARCHAR2(2000),
    ID                  VARCHAR2(50) NOT NULL,
    NAME                VARCHAR2(30),
    PASSWORD            VARCHAR2(20),
    REGDATE             DATE DEFAULT SYSDATE NOT NULL,
    LAST_UPDATE_DATE    DATE
)
TABLESPACE USERS
STORAGE
(
    INITIAL 64K
    NEXT 1M
)
NOCOMPRESS;

COMMENT ON COLUMN SCOTT.BOARD.BOARDNO IS '고유번호';
COMMENT ON COLUMN SCOTT.BOARD.TITLE IS '제목';
COMMENT ON COLUMN SCOTT.BOARD.CONTENTS IS '내용';
COMMENT ON COLUMN SCOTT.BOARD.ID IS '작성자ID';
COMMENT ON COLUMN SCOTT.BOARD.NAME IS '작성자이름';
COMMENT ON COLUMN SCOTT.BOARD.PASSWORD IS '비밀번호';
COMMENT ON COLUMN SCOTT.BOARD.REGDATE IS '작성시간';
COMMENT ON COLUMN SCOTT.BOARD.LAST_UPDATE_DATE IS '마지막수정시간';


-- Sequence
CREATE SEQUENCE SCOTT.BOARD_SEQ
MINVALUE 1
MAXVALUE 999999999999;